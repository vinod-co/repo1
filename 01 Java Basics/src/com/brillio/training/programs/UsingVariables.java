package com.brillio.training.programs;

public class UsingVariables {

	public static void main(String[] args) {
		
		int num;
		num = 123;
		byte b1 = 127;
		float f1 = 1.3e-2f;
		
		System.out.println("num is " + num);
		System.out.println("b1 is " + b1);
		System.out.println("f1 is " + f1);
		
	}
}
